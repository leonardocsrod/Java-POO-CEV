package condicaoif;

public class CondicaoIf {

    public static void main(String[] args) {
        int i = 10;
        if (i > 0){
            for(int j = 0; j < i; j++){
                System.out.println("" + j + "");
            }
            System.out.println("Fim da Contagem");
        }
        System.out.println("Fim do programa");
    }
    
    
}
